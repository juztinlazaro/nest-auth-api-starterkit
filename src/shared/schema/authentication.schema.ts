import * as mongoose from 'mongoose';

export const AuthenticationSchema = new mongoose.schema({
  auth_id: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  isVerified: {
    type: Boolean,
  },
});
